    private void cargar_ventas_general(String f){
        int total_caja=0;
        try {
            con_reporte_v = cc_reporte_v.conexion(user.get_ip());
            Statement st = con_reporte_v.createStatement();
            ResultSet result =st.executeQuery(  " SELECT  \n" +
                                                "    SUM(CASE WHEN `fecha_funcion`='"+f+"' THEN `costo` ELSE 0 END) as venta_dia,\n" +
                                                "    SUM(CASE WHEN `fecha_funcion`!='"+f+"' THEN `costo` ELSE 0 END) as preventa,\n" +
                                                "    IFNULL(SUM(`costo`),0) as total, COUNT(*) as cantidad\n" +
                                                "    FROM ventas\n" +
                                                "    WHERE DATE(ventas.`fecha_venta`)='"+f+"' AND usuario_id="+user.get_id()+"");

            if(result.next()){
                int vdia,pre,tot,cantv;
                
                vdia=result.getInt(1);
                pre=result.getInt(2);
                tot=result.getInt(3);
                cantv=result.getInt(4);
                
                venta_dia.setText(""+mil(vdia));
                preventa.setText(""+mil(pre));
                total.setText(""+mil(tot));
                cantidad_entradas.setText(""+cantv); 
                
                total_caja=tot;
            } 
 
            result =st.executeQuery("SELECT SUM(cantidad), SUM(valor)\n" +
                                    "FROM gafas where DATE(`fecha`)='"+f+"' AND usuario_id="+user.get_id()+"");
            if(result.next()){
                int gafas,cantg;
                
                cantg=result.getInt(1);
                gafas=result.getInt(2);
                
                cantidad_gafas.setText("Gafas("+cantg+")");
                Valor_gafas.setText(""+mil(gafas));   
                
                total_caja += gafas;
            }
            
            Total.setText(""+mil(total_caja));
            
            con_reporte_v.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"error 57:  "+ex);
        }
  
    }
    
    public String mil(int num){
        String numero="";
        DecimalFormat formatea = new DecimalFormat("###,###.##");
        
        return formatea.format(num);
    }
    
